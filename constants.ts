export const getSimulationRoute = (regionalName: string): RoutePoint[] => {
  const center = REGIONAL_CENTERS[regionalName] || REGIONAL_CENTERS["CENTRO-SUL"];
  const points: RoutePoint[] = [];
  const radius = 0.008; // ~800m
  const steps = 10;
  const now = Date.now();
  for (let i = 0; i < steps; i++) {
    const angle = (i / steps) * 2 * Math.PI;
    points.push({
      lat: center.lat + radius * Math.cos(angle),
      lng: center.lng + radius * Math.sin(angle),
      timestamp: now + i * 10000 
    });
  }
  return points;
};
